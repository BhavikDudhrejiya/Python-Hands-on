# Assigning Variables
var1 = 'Hello World' # String Variable
var2 = 4 # Integer
var3 = 36.7 # Float
var4 = 'This is a Python Tutorial'
var5 = '32'

# Concatenation of var1 and var2
print(var2 + var3)
print(var1 + ' ' + var4)
print(var1 + var5)
