#Escape Sequence = \n

#Printing a statement
print('Bhavik Dudhrejiya')

#Printing a statement with escape sequence using \n
print('Bhavik \nDudhrejiya')

#If any santences or words have \n**** then python will considered an escape sequence, Example as below
print('C:\name')

#Handling above issue by putting single quote(') between \ and n i.e. \'n
print('C:\'name')

#Escape Sequence = \t (tab)
print('Bhavik\tDudhrejiya')
print('Bha\tvik')
